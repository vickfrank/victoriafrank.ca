<script>

	jQuery(document).ready(function($)
	{
		var sh_selected = 'row';
		
		$( '.sh-list' ).change(function()
		{
			$( '.sh-wrap div' ).hide();
			
			sh_selected = $( '.sh-list' ).val();
			
			$( '.' + sh_selected ).show();
			
			if ( sh_selected == 'button' )
			{
				$( '.sh-button' ).show();
			}
		});
		
		
		$( '.sh-insert' ).click(function()
		{
			var sh_out = "";
			
			if ( sh_selected == 'row' )
			{
				sh_out = '[row][/row]';
			}
			else if ( sh_selected == 'column' )
			{
				var column_width = $( '#column_width' ).val();
				var column_offset = $( '#column_offset' ).val();
				var column_content = $( '#column_content' ).val();
				
				sh_out = '[column width="' + column_width + '" offset="' + column_offset + '"]' + column_content + '[/column]';
			}
			else if ( sh_selected == 'profile_images' )
			{
				sh_out = '[profile_images][/profile_images]';
			}
			else if ( sh_selected == 'profile_image' )
			{
				var profile_image_alt = $( '#profile_image_alt' ).val();
				var profile_image_title = $( '#profile_image_title' ).val();
				var profile_image_src = $( '#profile_image_src' ).val();
				
				sh_out = '[profile_image alt="' + profile_image_alt + '" title="' + profile_image_title + '" src="' + profile_image_src + '"]';
			}
			else if ( sh_selected == 'label' )
			{
				var label_content = $( '#label_content' ).val();
				
				sh_out = '[label]' + label_content + '[/label]';
			}
			else if ( sh_selected == 'social_icons' )
			{
				sh_out = '[social_icons][/social_icons]';
			}
			else if ( sh_selected == 'social_icon' )
			{
				var social_icon_type = $( '#social_icon_type' ).val();
				var social_icon_title = $( '#social_icon_title' ).val();
				var social_icon_url = $( '#social_icon_url' ).val();
				
				sh_out = '[social_icon type="' + social_icon_type + '" title="' + social_icon_title + '" url="' + social_icon_url + '"]';
			}
			else if ( sh_selected == 'personal_infos' )
			{
				sh_out = '[personal_infos][/personal_infos]';
			}
			else if ( sh_selected == 'personal_info' )
			{
				var personal_info_title = $( '#personal_info_title' ).val();
				var personal_info_content = $( '#personal_info_content' ).val();
				
				sh_out = '[personal_info title="' + personal_info_title + '"]' + personal_info_content + '[/personal_info]';
			}
			else if ( sh_selected == 'resume_group' )
			{
				var resume_group_title = $( '#resume_group_title' ).val();
				
				sh_out = '[resume_group title="' + resume_group_title + '"][/resume_group]';
			}
			else if ( sh_selected == 'history_unit' )
			{
				var history_unit_position = $( '#history_unit_position' ).val();
				var history_unit_company = $( '#history_unit_company' ).val();
				var history_unit_year = $( '#history_unit_year' ).val();
				var history_unit_content = $( '#history_unit_content' ).val();
				
				sh_out = '[history_unit position="' + history_unit_position + '" company="' + history_unit_company + '" year="' + history_unit_year + '"]' + history_unit_content + '[/history_unit]';
			}
			else if ( sh_selected == 'skill_unit' )
			{
				var skill_unit_progress = $( '#skill_unit_progress' ).val();
				var skill_unit_content = $( '#skill_unit_content' ).val();
				
				sh_out = '[skill_unit progress="' + skill_unit_progress + '"]' + skill_unit_content + '[/skill_unit]';
			}
			else if ( sh_selected == 'cv_button' )
			{
				var cv_button_text = $( '#cv_button_text' ).val();
				var cv_button_url = $( '#cv_button_url' ).val();
				
				sh_out = '[cv_button text="' + cv_button_text + '" url="' + cv_button_url + '"]';
			}
			else if ( sh_selected == 'map' )
			{
				var map_content = $( '#map_content' ).val();
				
				sh_out = '[map]' + map_content + '[/map]';
			}
			else if ( sh_selected == 'contact_form' )
			{
				var contact_form_to = $( '#contact_form_to' ).val();
				var contact_form_subject = $( '#contact_form_subject' ).val();
				
				sh_out = '[contact_form to="' + contact_form_to + '" subject="' + contact_form_subject + '"]';
			}
			else if ( sh_selected == 'lightbox' )
			{
				var lightbox_video = $( '#lightbox_video' ).val();
				var lightbox_title = $( '#lightbox_title' ).val();
				var lightbox_src = $( '#lightbox_src' ).val();
				
				sh_out = '[lightbox video="' + lightbox_video + '" title="' + lightbox_title + '" src="' + lightbox_src + '"]';
			}
			else if ( sh_selected == 'portfolio_field' )
			{
				var portfolio_field_content = $( '#portfolio_field_content' ).val();
				
				sh_out = '[portfolio_field]' + portfolio_field_content + '[/portfolio_field]';
			}
			else if ( sh_selected == 'image' )
			{
				var image_width = $( '#image_width' ).val();
				var image_height = $( '#image_height' ).val();
				var image_alt = $( '#image_alt' ).val();
				var image_title = $( '#image_title' ).val();
				var image_src = $( '#image_src' ).val();
				
				sh_out = '[image width="' + image_width + '" height="' + image_height + '" alt="' + image_alt + '" title="' + image_title + '" src="' + image_src + '"]';
			}
			else if ( sh_selected == 'tags' )
			{
				sh_out = '[tags][/tags]';
			}
			else if ( sh_selected == 'tag' )
			{
				var tag_content = $( '#tag_content' ).val();
				
				sh_out = '[tag]' + tag_content + '[/tag]';
			}
			else if ( sh_selected == 'launch_button' )
			{
				var launch_button_text = $( '#launch_button_text' ).val();
				var launch_button_url = $( '#launch_button_url' ).val();
				
				sh_out = '[launch_button text="' + launch_button_text + '" url="' + launch_button_url + '"]';
			}
			else if ( sh_selected == 'button' )
			{
				var button_text = $( '#button_text' ).val();
				var button_url = $( '#button_url' ).val();
				
				sh_out = '[button text="' + button_text + '" url="' + button_url + '"]';
			}
			else if ( sh_selected == 'inline_video' )
			{
				var inline_video_content = $( '#inline_video_content' ).val();
				
				sh_out = '[inline_video]' + inline_video_content + '[/inline_video]';
			}
			
			
			if ( window.tinyMCE )
			{
				window.tinyMCE.execInstanceCommand( 'content', 'mceInsertContent', false, sh_out );
				tb_remove();
			}
		});
	});
	
</script>